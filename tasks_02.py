# 7.task
# Запросите у пользователя число и вычислите сумму всех чисел от 1 до этого числа включительно, которые кратны 2 или 3.
num = int(input("Введите число: "))
total = 0
for i in range(1, num + 1):
    if i % 2 == 0 or i % 3 == 0:
        total += i
print("Сумма:", total)

