# 9.task
# Запросите у пользователя количество строк, а затем для каждой строки запросите текст. Подсчитайте и выведите количество строк, содержащих более 3 символов.
n = int(input("Введите количество строк: "))
count = 0
for _ in range(n):
    text = input("Введите строку: ")
    if len(text) > 3:
        count += 1
print("Количество строк с более чем 3 символами:", count)

